// Helper function to create slug-like ID from English title
function createTopicId(titleEn: string): string {
  return titleEn
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '') // Remove special characters except spaces and hyphens
    .replace(/\s+/g, '_') // Replace spaces with underscores
    .replace(/-/g, '_') // Replace hyphens with underscores
    .replace(/_+/g, '_') // Replace multiple underscores with single underscore
    .replace(/^_|_$/g, '') // Remove leading/trailing underscores
}

function createTopic(titleEn: string, universities: University[] = universitiesData): Topic {
  return {
    id: createTopicId(titleEn),
    name: titleEn,
    universities,
  }
}

